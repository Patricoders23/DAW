function sumar(n1, n2){
    return alert (`El resultado de sumar ${n1} y ${n2} es: ${parseInt(n1) + parseInt(n2)}`);
}
function restar(n1, n2){
    return alert (`El resultado de restar ${n1} y ${n2} es: ${parseInt(n1) - parseInt(n2)}`);
}
function dividir(n1, n2){
    return alert (`El resultado de dividir ${n1} y ${n2} es: ${parseInt(n1) / parseInt(n2)}`);
}
function multiplicar(n1, n2){
    return alert (`El resultado de multiplicar ${n1} y ${n2} es: ${parseInt(n1) * parseInt(n2)}`);
}

